package ch10.sub4ReplaceConditionalWithPolymorphism;

public class BeforeBird {

    public String name;
    public String type;

    public int numberOfCoconuts;
    public int voltage;
    public boolean isNailed;

    public BeforeBird(String name, String type, int numberOfCoconuts, int voltage, boolean isNailed) {
        this.name = name;
        this.type = type;
        this.numberOfCoconuts = numberOfCoconuts;
        this.voltage = voltage;
        this.isNailed = isNailed;
    }
}
